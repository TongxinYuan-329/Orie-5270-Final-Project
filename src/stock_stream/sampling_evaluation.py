"""Evaluation helpers for stream sampling algorithms."""

from __future__ import annotations

import json
import time
import tracemalloc
from collections import Counter
from collections.abc import Callable, Sequence
from dataclasses import asdict
from pathlib import Path
from typing import Any

import pandas as pd

from stock_stream.heap_sampler import heap_sample
from stock_stream.reservoir import reservoir_sample
from stock_stream.stream import dataframe_to_stream

SamplerFn = Callable[[Sequence[Any], int, int | None], list[Any]]


def _item_key(item: Any) -> str:
    if hasattr(item, "date") and hasattr(item, "ticker"):
        return f"{item.date}|{item.ticker}"
    return repr(item)


def _normalize_sample(sample: Sequence[Any]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for item in sample:
        if hasattr(item, "__dataclass_fields__"):
            rows.append(asdict(item))
        elif isinstance(item, dict):
            rows.append(dict(item))
        else:
            rows.append({"value": item})
    return rows


def load_stream_items(clean_csv_path: Path) -> list[Any]:
    """Load stream items from cleaned csv generated by step 1."""

    frame = pd.read_csv(clean_csv_path, parse_dates=["date"])
    return list(dataframe_to_stream(frame))


def run_correctness_experiment(
    stream_items: Sequence[Any], k: int, runs: int = 500
) -> tuple[pd.DataFrame, pd.DataFrame]:
    """Repeat sampling and collect per-observation inclusion frequencies."""

    methods: dict[str, SamplerFn] = {
        "reservoir": reservoir_sample,
        "heap": heap_sample,
    }
    all_rows: list[dict[str, Any]] = []
    summary_rows: list[dict[str, Any]] = []

    for method_name, sampler in methods.items():
        counts: Counter[str] = Counter()
        for seed in range(runs):
            sample = sampler(stream_items, k, seed=seed)
            for sampled in sample:
                counts[_item_key(sampled)] += 1

        expected = runs * (k / len(stream_items))
        freqs = [counts[_item_key(item)] / runs for item in stream_items]
        summary_rows.append(
            {
                "method": method_name,
                "k": k,
                "runs": runs,
                "expected_selection_prob": k / len(stream_items),
                "mean_selection_prob": float(sum(freqs) / len(freqs)),
                "std_selection_prob": float(pd.Series(freqs).std(ddof=1)),
                "max_abs_count_error_vs_expected": float(
                    max(abs(count - expected) for count in counts.values())
                ),
            }
        )

        for item in stream_items:
            key = _item_key(item)
            all_rows.append(
                {
                    "method": method_name,
                    "item_key": key,
                    "selected_count": counts[key],
                    "selection_prob": counts[key] / runs,
                }
            )

    return pd.DataFrame(all_rows), pd.DataFrame(summary_rows)


def run_runtime_experiment(
    stream_items: Sequence[Any],
    n_values: Sequence[int],
    k_values: Sequence[int],
    repeats: int = 5,
) -> pd.DataFrame:
    """Compare runtime across different stream lengths and sample sizes."""

    methods: dict[str, SamplerFn] = {
        "reservoir": reservoir_sample,
        "heap": heap_sample,
    }
    rows: list[dict[str, Any]] = []

    for n in n_values:
        subset = stream_items[:n]
        for k in k_values:
            for method_name, sampler in methods.items():
                elapsed_ms: list[float] = []
                for rep in range(repeats):
                    start = time.perf_counter()
                    sampler(subset, k, seed=rep)
                    elapsed_ms.append((time.perf_counter() - start) * 1000.0)
                rows.append(
                    {
                        "method": method_name,
                        "stream_length_n": n,
                        "k": k,
                        "repeats": repeats,
                        "runtime_ms_mean": float(pd.Series(elapsed_ms).mean()),
                        "runtime_ms_std": float(pd.Series(elapsed_ms).std(ddof=1)),
                    }
                )

    return pd.DataFrame(rows).sort_values(["stream_length_n", "k", "method"]).reset_index(drop=True)


def _peak_memory_bytes(run_fn: Callable[[], None]) -> int:
    tracemalloc.start()
    run_fn()
    _, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()
    return peak


def run_memory_experiment(stream_items: Sequence[Any], k_values: Sequence[int]) -> pd.DataFrame:
    """Compare peak memory of full-history, reservoir, and heap sampling."""

    rows: list[dict[str, Any]] = []
    n = len(stream_items)

    baseline_peak = _peak_memory_bytes(lambda: list(stream_items))
    rows.append(
        {
            "method": "full_history_baseline",
            "n": n,
            "k": None,
            "peak_memory_bytes": baseline_peak,
        }
    )

    for k in k_values:
        reservoir_peak = _peak_memory_bytes(lambda: reservoir_sample(stream_items, k, seed=0))
        heap_peak = _peak_memory_bytes(lambda: heap_sample(stream_items, k, seed=0))
        rows.append({"method": "reservoir", "n": n, "k": k, "peak_memory_bytes": reservoir_peak})
        rows.append({"method": "heap", "n": n, "k": k, "peak_memory_bytes": heap_peak})

    return pd.DataFrame(rows).sort_values(["method", "k"], na_position="first").reset_index(drop=True)


def save_sampling_outputs(
    output_dir: Path,
    sample_rows: Sequence[Any],
    correctness_freq: pd.DataFrame,
    correctness_summary: pd.DataFrame,
    runtime_df: pd.DataFrame,
    memory_df: pd.DataFrame,
) -> dict[str, Path]:
    """Persist all sampling outputs using a unified sample format."""

    output_dir.mkdir(parents=True, exist_ok=True)
    paths = {
        "sample_json": output_dir / "sampling_sample_preview.json",
        "correctness_freq_csv": output_dir / "sampling_correctness_frequencies.csv",
        "correctness_summary_csv": output_dir / "sampling_correctness_summary.csv",
        "runtime_csv": output_dir / "sampling_runtime_comparison.csv",
        "memory_csv": output_dir / "sampling_memory_comparison.csv",
    }

    paths["sample_json"].write_text(
        json.dumps(_normalize_sample(sample_rows), indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    correctness_freq.to_csv(paths["correctness_freq_csv"], index=False)
    correctness_summary.to_csv(paths["correctness_summary_csv"], index=False)
    runtime_df.to_csv(paths["runtime_csv"], index=False)
    memory_df.to_csv(paths["memory_csv"], index=False)
    return paths


def run_all_sampling_evaluations(
    clean_csv_path: Path,
    output_dir: Path,
    sample_k: int = 50,
    correctness_runs: int = 500,
    runtime_n: Sequence[int] = (1_000, 10_000, 100_000),
    runtime_k: Sequence[int] = (10, 50, 100),
) -> dict[str, Path]:
    """Run correctness/runtime/memory experiments and save outputs."""

    stream_items = load_stream_items(clean_csv_path)
    if not stream_items:
        raise ValueError("stream is empty")

    available_n = [n for n in runtime_n if n <= len(stream_items)]
    if not available_n:
        available_n = [len(stream_items)]
    elif available_n[-1] != len(stream_items):
        available_n.append(len(stream_items))

    sample_preview = reservoir_sample(stream_items, sample_k, seed=42)
    correctness_freq, correctness_summary = run_correctness_experiment(
        stream_items, k=sample_k, runs=correctness_runs
    )
    runtime_df = run_runtime_experiment(
        stream_items, n_values=available_n, k_values=runtime_k, repeats=5
    )
    memory_df = run_memory_experiment(stream_items, k_values=runtime_k)
    return save_sampling_outputs(
        output_dir=output_dir,
        sample_rows=sample_preview,
        correctness_freq=correctness_freq,
        correctness_summary=correctness_summary,
        runtime_df=runtime_df,
        memory_df=memory_df,
    )
